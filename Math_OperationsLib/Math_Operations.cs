namespace Math_OperationsLib
{
    public class Math_Operations
    {
        public int Sum(int x, int y)
        {
            return x + y;
        }
        public int Subtract(int x, int y)
        {
            return x - y;
        }
        
        public int Divide(int x, int y)
        {
            return x / y;
        }
        public int Multiplex(int x, int y)
        {
            return x * y;
        }
    }
}
